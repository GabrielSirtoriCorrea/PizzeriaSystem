            PriceLabel[i].setIcon(new javax.swing.ImageIcon("C:\\Users\\Gazebo\\Documents\\ProjetosGithub\\SistemaPizzaria\\Images\\Imagem Pre√ßo.png"));
            
            RequestPanel.add(label[i]);
